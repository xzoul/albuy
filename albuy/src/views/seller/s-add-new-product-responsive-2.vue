          await this.ProductVariations.forEach(async (variation, i)=>{
            var param = {
              "ProductID" : res,
              "Type" : variation.Type,
            };
            await Parse.Cloud.run("AddVariationType", param).then(async (res2)=>{
              await variation.Options.forEach(async (option, j)=>{
                var param2 = {
                  "VariationTypeID" : res2,
